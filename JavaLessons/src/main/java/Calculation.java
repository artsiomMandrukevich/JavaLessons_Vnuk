//class which contains simple arithmetical methods
public class Calculation {

    public int getSum(int firstValue, int secondValue) {
        int summ = firstValue + secondValue;
        return summ;
    }

    public int getSubtraction(int firstValue, int secondValue) {
        return firstValue - secondValue;
    }

    public int getMultiplication(int firstValue, int secondValue) {
        return firstValue*secondValue;
    }

    public int getDivision(int firstValue, int secondValue) {
        return firstValue/secondValue;
    }
}
